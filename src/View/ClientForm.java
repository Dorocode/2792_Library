    private void btnBrwActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrwActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        String fileName = file.getAbsolutePath();
        txtPhotoPath.setText(fileName);
        ImageIcon icon = new ImageIcon(fileName);
        Image img = icon.getImage();
        Image imageScale = img.getScaledInstance(photoo.getWidth(), photoo.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imageScale);
        photoo.setIcon(scaledIcon);
    }//GEN-LAST:event_btnBrwActionPerformed

