    private void clientTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clientTableMouseClicked
        // TODO add your handling code here:
        int row = clientTable.getSelectedRow();
        regnotxt.setText(model.getValueAt(row, 0).toString());
        txtfname.setText(model.getValueAt(row, 1).toString());
        txtlname.setText(model.getValueAt(row, 2).toString());
        phoneno.setText(model.getValueAt(row, 3).toString());
        emailtxt.setText(model.getValueAt(row, 4).toString());
        comboCat.setSelectedItem(model.getValueAt(row, 5).toString());
        txtPhotoPath.setText(model.getValueAt(row, 6).toString());
    }//GEN-LAST:event_clientTableMouseClicked


